class Product {
  final String id;
  final String title;
  final String gambar;
  final int isActive;
  final String createTime;
  final String updateTime;
  final String price;

  Product({
    required this.id,
    required this.title,
    required this.gambar,
    required this.isActive,
    required this.createTime,
    required this.updateTime,
    required this.price,
  });


}